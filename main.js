
// Omid N.'s in-class math program
var a = prompt("Please pick first number: ");
var b = prompt("Please pick second number: ");
var c = prompt("Please pick operator (*,+,-,/,%): ");
switch (c){
  case "+":
      console.log(a+b);
      break;
  case "-":
      console.log(a-b);
      break;
  case "*":
      console.log(a*b);
      break;
  case "/":
      console.log(a/b);
      break;
  case "%":
      console.log(a%b);
      break;
}
